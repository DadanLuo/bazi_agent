# 安全检查函数
def check_safety(text: str) -> Dict[str, Any]:
    """检查文本是否安全"""
    sensitive_keywords = [
        "自杀", "自残", "死亡", "放弃", "绝望", "抑郁", "焦虑",
        "崩溃", "撑不住", "不想活", "跳楼", "割腕", " overdose"
    ]
    
    has_sensitive = any(keyword in text for keyword in sensitive_keywords)
    
    return {
        "is_safe": not has_sensitive,
        "has_sensitive_content": has_sensitive,
        "sensitive_keywords_found": [k for k in sensitive_keywords if k in text]
    }

# 危机干预检测
def detect_crisis(text: str) -> Dict[str, Any]:
    """检测危机干预需求"""
    crisis_keywords = [
        "不想活了", "想死", "自杀", "自残", "结束生命", "离开这个世界",
        "撑不住了", "太痛苦了", "无法承受", "绝望", "放弃"
    ]
    
    detected = [keyword for keyword in crisis_keywords if keyword in text]
    
    return {
        "needs_intervention": len(detected) > 0,
        "detected_keywords": detected,
        "severity": "high" if len(detected) > 2 else "medium" if len(detected) > 0 else "low"
    }
