"""塔罗牌领域数据 — 牌组、牌阵、位置定义"""
from typing import List, Dict
from enum import Enum
from dataclasses import dataclass


class Suit(str, Enum):
    """花色"""
    MAJOR = "major"
    WANDS = "wands"
    CUPS = "cups"
    SWORDS = "swords"
    PENTACLES = "pentacles"


class Orientation(str, Enum):
    """正逆位"""
    UPRIGHT = "upright"
    REVERSED = "reversed"


@dataclass
class TarotCard:
    """单张塔罗牌"""
    id: int
    name_en: str
    name_cn: str
    suit: Suit
    number: int
    upright_keywords: List[str]
    reversed_keywords: List[str]
    description: str


@dataclass
class SpreadPosition:
    """牌阵中的一个位置"""
    index: int
    name: str
    description: str


@dataclass
class Spread:
    """牌阵定义"""
    id: str
    name_cn: str
    positions: List[SpreadPosition]
    description: str
    suitable_for: List[str]

    @property
    def card_count(self) -> int:
        return len(self.positions)


